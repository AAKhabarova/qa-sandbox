/*  Импорт функции exec из встроенного модуля Node.js. Она выполняет shell-команды прямо из JS/TS кода  */
import { exec } from 'child_process';
  /*   Экспорт функции, которую Playwright автоматически вызывает после завершения всех тестов (благодаря настройке globalTeardown 
  в конфиге).  */
  export default async function globalTeardown() {                                                                            
    if (process.env.CI) {
      exec('docker compose down');                                                                                            
    }                                                                                                                       
  }

/*
                                                                                                                            
  if (process.env.CI) {                                                                                                       
  Проверка: мы в CI-окружении или нет? В GitHub Actions переменная CI=true выставляется автоматически.                        
                                                                                                                              
  - В CI (CI=true) — выполняем docker compose down, потому что за собой нужно убрать                                          
  - Локально (CI не задана) — пропускаем, чтобы контейнеры остались работать и не ждать перезапуска при следующем прогоне     
  тестов                                                                                                                      
                                                                                                                              
  exec('docker compose down');                                                                                                
  Выполняет команду в терминале — останавливает и удаляет все контейнеры, поднятые через docker compose up.                 
                                                                                                                              
  Итого
                                                                                                                              
  Это функция-уборщик: после тестов в CI гасит Docker-контейнеры, а локально оставляет их работать для удобства разработки. 
*/